import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import openapiTS, { astToString } from 'openapi-typescript';
import yaml from 'js-yaml';
import './generate-openapi-snapshot.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

const sourceSchemaPath = path.join(projectRoot, 'openapi', 'api-schema-2.yaml');
const outputDir = path.join(projectRoot, 'src', 'lib', 'generated');
const outputPath = path.join(outputDir, 'schema-types.ts');

await mkdir(outputDir, { recursive: true });

const sourceSchemaRaw = await readFile(sourceSchemaPath, 'utf-8');
const sourceSchema = yaml.load(sourceSchemaRaw);

const generatedAst = await openapiTS(sourceSchema, {
  alphabetize: true,
  exportType: true,
  immutable: true,
});
const generated = astToString(generatedAst);

const content = `/* eslint-disable */\n/*\n * This file is generated by libs/myos-js/scripts/generate-schema-types.mjs\n * from libs/myos-js/openapi/api-schema-2.yaml.\n * Do not edit manually.\n */\n\n${generated}`;

await writeFile(outputPath, content, 'utf-8');
console.log(`Generated schema types: ${outputPath}`);
